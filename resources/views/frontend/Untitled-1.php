        function inviteFriend() {
            var type = ""
            var input = ""
            var name = ""
            if ($("#whatsappNumber").val() != undefined) {
                type = "WN";
                input = $("#whatsappNumber").val();
                name = $("#name").val();
            } else if ($("#emailAddress").val() != undefined) {
                type = "EA";
                input = $("#emailAddress").val();
            } else {
                return false;
            }
            if (type == "WN" || type == "EA") {
                $.ajax({
                    type: "POST",
                    url: "https://www.kayasthasamaj.in/home/inviteFriend",
                    data: {
                        type: type,
                        inputVal: input,
                        name: name
                    },
                    dataType: "json",
                    success: function(response) {
                        if (response.success) {
                            $("#whatsappNumber").val("");
                            $("#name").val("");
                            $("#emailAddress").val("");
                            $("#inviteMsg").show()
                            $("#inviteMsg").html("<strong>Success!</strong> Invitation sent successfully.")
                        }
                    }
                });
            }
            return false;
        }

